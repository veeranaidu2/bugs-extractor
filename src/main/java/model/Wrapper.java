package model;

import java.net.URL;
import java.util.Date;

public class Wrapper {

    String filename;
    String message;
    URL url;
    Date date;

    public String getFilename() {
        return filename;
    }

    public String getMessage() {
        return message;
    }

    public URL getUrl() {
        return url;
    }

    public Date getDate() {
        return date;
    }

}
